    private void ButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonBuscarActionPerformed
        // TODO add your handling code here:
        int numeroCarnet = Integer.valueOf(this.FieldBuscarCarnet.getText());
        Estudiante newEstudiante = new Estudiante(numeroCarnet, "", "");
        Estudiante tmp = datosSistema.getEstudiantes().buscar(newEstudiante, newEstudiante.hashCode());
        if(tmp!=null){
            this.FieldCarnte.setText(String.valueOf(tmp.getCarnet()));
            this.FiledNombre.setText(tmp.getNombre());
            this.FieldDireccion.setText(tmp.getDireccion());
            this.FiledNombre.setEditable(true);
            this.FieldDireccion.setEditable(true);
            this.ButtonModificar.setEnabled(true);
        }else{
            JOptionPane.showMessageDialog(this, "No existe un estudiante con el numero de carnet \""+newEstudiante.getCarnet()+"\"","Error de busqueda", JOptionPane.WARNING_MESSAGE);
            this.FieldCarnte.setText(null);
            this.FiledNombre.setText(null);
            this.FieldDireccion.setText(null);
            this.FiledNombre.setEditable(false);
            this.FieldDireccion.setEditable(false);
            this.ButtonModificar.setEnabled(false);
        }
    }//GEN-LAST:event_ButtonBuscarActionPerformed

    private void ButtonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonModificarActionPerformed
        // TODO add your handling code here:
        int numeroCarnet = Integer.valueOf(this.FieldCarnte.getText());
        
        String nombre = this.FiledNombre.getText();
        String direccion = this.FieldDireccion.getText();
        
        Estudiante newEstudiante = new Estudiante(numeroCarnet, "", "");
        Estudiante tmp = datosSistema.getEstudiantes().buscar(newEstudiante, newEstudiante.hashCode());
        
        if(tmp!=null){
            if(comprobarAlfanumerico(nombre)){
                if(comprobarAlfanumerico(direccion)){
                    tmp.setNombre(nombre);
                    tmp.setDireccion(direccion);
                    JOptionPane.showMessageDialog(this, "Estudiante modficado con exito","Modificacion", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(this, "La direccion debe contener caracteres Alfanumericos","Error de modificacion", JOptionPane.WARNING_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(this, "El nombre debe contener caracteres Alfanumericos","Error de modificacion", JOptionPane.WARNING_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "No existe un estudiante con el numero de carnet \""+newEstudiante.getCarnet()+"\"","Error de modificacion", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_ButtonModificarActionPerformed


